def _limpiar(X):
    return np.nan_to_num(X.astype(np.float64), nan=0.0, posinf=0.0, neginf=0.0)

def escalar_fold(X_tr, X_vl):
    X_tr = _limpiar(X_tr); X_vl = _limpiar(X_vl)
    scalers  = {}
    n_tr, ts, nf = X_tr.shape
    n_vl         = X_vl.shape[0]
    for i in range(nf):
        scalers[i] = RobustScaler()
        tr_flat    = X_tr[:, :, i].reshape(-1, 1)
        vl_flat    = X_vl[:, :, i].reshape(-1, 1)
        scalers[i].fit(tr_flat)
        X_tr[:, :, i] = scalers[i].transform(tr_flat).reshape(n_tr, ts)
        X_vl[:, :, i] = scalers[i].transform(vl_flat).reshape(n_vl, ts)
    return X_tr, X_vl, scalers

def escalar_externo(X, scalers):
    X = _limpiar(X)
    n, ts, nf = X.shape
    for i in range(nf):
        feat       = X[:, :, i].reshape(-1, 1)
        X[:, :, i] = scalers[i].transform(feat).reshape(n, ts)
    return X

def ajustar_y_escalar(X):
    X = _limpiar(X)
    scalers  = {}
    n, ts, nf = X.shape
    for i in range(nf):
        scalers[i] = RobustScaler()
        feat       = X[:, :, i].reshape(-1, 1)
        scalers[i].fit(feat)
        X[:, :, i] = scalers[i].transform(feat).reshape(n, ts)
    return X, scalers

def crear_dataset_tf(X: np.ndarray, y_reg: np.ndarray, y_tend: np.ndarray,
                     weights: np.ndarray = None, batch_size: int = BATCH_SIZE,
                     shuffle: bool = True) -> tf.data.Dataset:
    outputs = {
        'out_regresion':    y_reg.astype(np.float32),
        'out_clasificacion': y_tend.astype(np.int32),
    }
    if weights is not None:
        ds = tf.data.Dataset.from_tensor_slices((X.astype(np.float32), outputs, weights.astype(np.float32)))
    else:
        ds = tf.data.Dataset.from_tensor_slices((X.astype(np.float32), outputs))

    if shuffle:
        ds = ds.shuffle(buffer_size=min(len(X), 50_000), seed=SEMILLA, reshuffle_each_iteration=True)
    return ds.batch(batch_size).prefetch(tf.data.AUTOTUNE)

def filtrar_mantenimiento_redundante(df_train: pd.DataFrame, umbral_var: float = 1.0, ventana: int = 12) -> pd.DataFrame:
    def _filtrar_paciente(grupo):
        fase = grupo['FASE_PROXY'].values
        psi  = grupo['PSI'].values
        eff  = grupo['EFF'].values
        mantener = np.ones(len(grupo), dtype=bool)
        for i in range(len(grupo)):
            if fase[i] == 'Mantenimiento':
                inicio   = max(0, i - ventana)
                var_local = np.var(psi[inicio:i+1]) + np.var(eff[inicio:i+1])
                if var_local < umbral_var:
                    mantener[i] = False
        return grupo[mantener]

    df_filtrado = df_train.groupby('ID_ANONIMO', group_keys=False).apply(_filtrar_paciente).reset_index(drop=True)
    return df_filtrado

def calcular_pesos_psi_clinico(yr_norm, psi_mean, psi_std):
    psi_real = yr_norm * psi_std + psi_mean
    pesos = np.where(psi_real >= 50.0, 2.0,
            np.where(psi_real >= 25.0, 1.5,
            np.where(psi_real <  25.0, 1.2, 1.0)))
    return pesos.astype(np.float32)

def agregar_features_eff(grupo: pd.DataFrame) -> pd.DataFrame:
    df = grupo.copy()
    df['PRESION_PULSO'] = (df['PSIS'] - df['PDIA'] if ('PSIS' in df.columns and 'PDIA' in df.columns) else 0.0)
    df['FC_vel'] = df['FC'].diff().fillna(0)
    df['FC_trend_3min'] = df['FC'].rolling(36, min_periods=1).apply(lambda x: x.iloc[-1] - x.iloc[0], raw=False)

    DT_SEG = 5.0
    df['INF']  = df['INF'].fillna(0)
    df['INF2'] = df['INF2'].fillna(0)
    peso_safe  = np.where(df['PESO'].values > 0, df['PESO'].values, 70.0)

    df['DOSIS_PROP_PASO'] = (df['INF'] / 3600.0) * DT_SEG * 10.0
    if 'DILUCION_2' in df.columns:
        df['DOSIS_REMI_PASO'] = (df['INF2'] / 3600.0) * DT_SEG * df['DILUCION_2']
    else:
        df['DOSIS_REMI_PASO'] = (df['INF2'] / 3600.0) * DT_SEG

    df['DOSIS_PROP_ACUM_KG'] = df['DOSIS_PROP_PASO'].rolling(60, min_periods=1).sum() / peso_safe
    df['DOSIS_REMI_ACUM_KG'] = df['DOSIS_REMI_PASO'].rolling(60, min_periods=1).sum() / peso_safe

    df['INF_vel']  = df['INF'].diff().fillna(0)
    df['INF2_vel'] = df['INF2'].diff().fillna(0)

    def _estado_bomba(vel, inf):
        return np.where(inf == 0, -2, np.where(vel > 0, 1, np.where(vel < 0, -1, 0))).astype(np.float32)

    df['INF_dir4']  = _estado_bomba(df['INF_vel'].values,  df['INF'].values)
    df['INF2_dir4'] = _estado_bomba(df['INF2_vel'].values, df['INF2'].values)

    if 'TARGET' in df.columns:
        df['ERROR_TCI']  = df['TARGET']  - df['EFF']
        df['TARGET_vel'] = df['TARGET'].diff().fillna(0)
    else:
        df['ERROR_TCI'] = df['TARGET_vel'] = 0.0

    if 'TARGET2' in df.columns:
        df['ERROR_TCI2']  = df['TARGET2'] - df['EFF2']
        df['TARGET2_vel'] = df['TARGET2'].diff().fillna(0)
    else:
        df['ERROR_TCI2'] = df['TARGET2_vel'] = 0.0

    df['EFF_vel']  = df['EFF'].diff().fillna(0)
    df['EFF2_vel'] = df['EFF2'].diff().fillna(0)
    df['EFF_acel']  = df['EFF_vel'].diff().fillna(0)
    df['EFF2_acel'] = df['EFF2_vel'].diff().fillna(0)

    df['EFF_trend_3min']  = df['EFF'].rolling(36, min_periods=1).apply(lambda x: x.iloc[-1] - x.iloc[0], raw=False)
    df['EFF2_trend_3min'] = df['EFF2'].rolling(36, min_periods=1).apply(lambda x: x.iloc[-1] - x.iloc[0], raw=False)
    df['EFF_ratio'] = df['EFF'] / (df['EFF2'] + 0.01)
    df['EFF_auc_5min']  = df['EFF'].rolling(60,  min_periods=1).mean()
    df['EFF2_auc_5min'] = df['EFF2'].rolling(60, min_periods=1).mean()
    df['EFF_caida_pico']  = df['EFF'].rolling(120, min_periods=1).max() - df['EFF']
    df['EFF2_caida_pico'] = df['EFF2'].rolling(120, min_periods=1).max() - df['EFF2']

    return df

def crear_secuencias_paciente(grupo, lista_features):
    vals  = grupo[lista_features].values
    psi   = grupo['PSI'].values
    fases = grupo['FASE_PROXY'].values
    X_seq, y_reg, y_tend, phase_seq = [], [], [], []

    for i in range(len(grupo) - TIMESTEPS - HORIZONTE):
        X_seq.append(vals[i: i + TIMESTEPS])
        psi_actual = psi[i + TIMESTEPS - 1]
        y_reg.append(psi_actual)
        psi_futuro = psi[i + TIMESTEPS + HORIZONTE - 1]
        delta = psi_futuro - psi_actual
        if delta > UMBRAL_TENDENCIA:
            y_tend.append(2)
        elif delta < -UMBRAL_TENDENCIA:
            y_tend.append(0)
        else:
            y_tend.append(1)
        phase_seq.append(fases[i + TIMESTEPS - 1])

    return X_seq, y_reg, y_tend, phase_seq

def construir_dataset(df_in, lista_features):
    X_all, yr_all, yt_all, gr_all, ph_all = [], [], [], [], []
    for pid, grupo in df_in.groupby('ID_ANONIMO'):
        if len(grupo) > TIMESTEPS + HORIZONTE:
            Xs, yr, yt, ph = crear_secuencias_paciente(grupo, lista_features)
            X_all.extend(Xs);  yr_all.extend(yr)
            yt_all.extend(yt); ph_all.extend(ph)
            gr_all.extend([pid] * len(Xs))
    return (np.array(X_all), np.array(yr_all), np.array(yt_all), np.array(gr_all), np.array(ph_all))

def calcular_metricas_pk(y_real, y_pred, limite=10.0):
    pe     = ((y_pred - y_real) / (y_real + 1e-8)) * 100.0
    ape    = np.abs(pe)
    mdpe   = np.median(pe)
    mdape  = np.median(ape)
    wobble = np.median(np.abs(pe - mdpe))
    pct    = np.mean(np.abs(y_pred - y_real) <= limite) * 100.0
    rmse   = np.sqrt(np.mean((y_pred - y_real)**2))
    return {
        'MAE':              mean_absolute_error(y_real, y_pred),
        'RMSE':             rmse,
        'R2':               r2_score(y_real, y_pred),
        'MDPE (%)':         mdpe,
        'MDAPE (%)':        mdape,
        'Wobble (%)':       wobble,
        f'% ±{limite:.0f} PSI': pct,
    }

