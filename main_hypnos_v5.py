def build_chimera_model(timesteps, n_features):
    entradas = Input(shape=(timesteps, n_features))
    x = GRU(128, return_sequences=True, activation='tanh',
             recurrent_dropout=0.05, name='gru_1', dtype='float32')(entradas)
    x = GRU(64,  return_sequences=False, activation='tanh',
             recurrent_dropout=0.05, name='gru_2', dtype='float32')(x)
    x = BatchNormalization(name='bn_shared')(x)
    x = Dropout(0.3)(x)
    x = Dense(64, activation='swish', name='dense_shared')(x)

    reg     = Dense(32, activation='swish', name='dense_reg')(x)
    out_reg = Dense(1, activation='linear', name='out_regresion', dtype='float32')(reg)

    psi_hat_sg = Lambda(lambda t: tf.stop_gradient(tf.cast(t, tf.float32)), name='stop_gradient')(out_reg)
    x_cond  = Concatenate(name='concat_reg_cls')([x, psi_hat_sg])
    cls     = Dense(32, activation='swish', name='dense_cls')(x_cond)
    out_cls = Dense(3, activation='softmax', name='out_clasificacion', dtype='float32')(cls)

    return Model(inputs=entradas, outputs=[out_reg, out_cls])

def compilar_fase1(model):
    for capa in ['dense_cls', 'out_clasificacion']:
        model.get_layer(capa).trainable = False
    lr = tf.keras.optimizers.schedules.CosineDecay(0.002, 20000, alpha=0.01)
    model.compile(
        optimizer=tf.keras.optimizers.AdamW(lr, weight_decay=1e-4),
        loss={'out_regresion': tf.keras.losses.MeanSquaredError(), 'out_clasificacion': FocalLoss(gamma=3.0)},
        loss_weights={'out_regresion': 1.0, 'out_clasificacion': 0.0},
        metrics={'out_regresion': 'mae', 'out_clasificacion': 'accuracy'},
        jit_compile="auto")
    return model

def compilar_fase2(model):
    for layer in model.layers:
        layer.trainable = True
    for capa in ['gru_1', 'gru_2', 'bn_shared', 'dense_shared', 'dense_reg']:
        model.get_layer(capa).trainable = False
    lr = tf.keras.optimizers.schedules.CosineDecay(0.0005, 12000, alpha=0.01)
    model.compile(
        optimizer=tf.keras.optimizers.AdamW(lr, weight_decay=1e-4),
        loss={'out_regresion': tf.keras.losses.MeanSquaredError(), 'out_clasificacion': FocalLoss(gamma=3.0)},
        loss_weights={'out_regresion': 0.0, 'out_clasificacion': 1.0},
        metrics={'out_regresion': 'mae', 'out_clasificacion': 'accuracy'},
        jit_compile="auto")
    return model

def oversample_clases_minoritarias(X_tr, yr_tr, yt_tr, factor=4):
    idx_est  = np.where(yt_tr == 1)[0]
    idx_desc = np.tile(np.where(yt_tr == 0)[0], factor)
    idx_asc  = np.tile(np.where(yt_tr == 2)[0], factor)
    idx      = np.concatenate([idx_est, idx_desc, idx_asc])
    np.random.shuffle(idx)
    return X_tr[idx], yr_tr[idx], yt_tr[idx]

def balancear_fases(X_tr, yr_tr, yt_tr, phases_tr, ratio_max_mantenimiento=0.55):
    idx_mant  = np.where(phases_tr == 'Mantenimiento')[0]
    idx_otras = np.where(phases_tr != 'Mantenimiento')[0]
    n_otras   = len(idx_otras)
    n_max     = min(int(n_otras * ratio_max_mantenimiento / (1 - ratio_max_mantenimiento)), len(idx_mant))
    idx_sub   = np.random.choice(idx_mant, n_max, replace=False)
    idx_fin   = np.concatenate([idx_otras, idx_sub])
    np.random.shuffle(idx_fin)
    return (X_tr[idx_fin], yr_tr[idx_fin], yt_tr[idx_fin], phases_tr[idx_fin])

N_MC_MUESTRAS = 50

def predecir_con_incertidumbre(model, X, n_muestras=N_MC_MUESTRAS):
    X_tensor = tf.constant(X.astype(np.float32))
    psi_muestras, tend_muestras = [], []
    for _ in range(n_muestras):
        psi_n, tend_p = model(X_tensor, training=True)
        psi_muestras.append(np.nan_to_num(tf.cast(psi_n, tf.float32).numpy().flatten(), nan=0.0))
        tend_muestras.append(np.nan_to_num(tf.cast(tend_p, tf.float32).numpy(), nan=0.0))
    psi_arr  = np.array(psi_muestras)
    tend_arr = np.array(tend_muestras)
    return psi_arr.mean(axis=0), psi_arr.std(axis=0), tend_arr.mean(axis=0)

