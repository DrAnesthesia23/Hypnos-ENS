class FocalLoss(tf.keras.losses.Loss):
    """Focal Loss con reduction=NONE para compatibilidad Keras 3 multi-output."""
    def __init__(self, gamma: float = 3.0, name: str = 'focal_loss'):
        super().__init__(reduction=tf.keras.losses.Reduction.NONE, name=name)
        self.gamma = gamma

    def call(self, y_true, y_pred):
        y_true    = tf.cast(tf.reshape(y_true, [-1]), tf.int32)
        y_pred    = tf.cast(y_pred, tf.float32)
        y_pred    = tf.clip_by_value(y_pred, 1e-7, 1.0 - 1e-7)
        ce        = tf.keras.losses.sparse_categorical_crossentropy(y_true, y_pred)
        y_true_oh = tf.one_hot(y_true, depth=tf.shape(y_pred)[-1])
        p_t       = tf.reduce_sum(y_pred * y_true_oh, axis=-1)
        return tf.pow(1.0 - p_t, self.gamma) * ce

    def get_config(self):
        return {'gamma': self.gamma}

class MacroF1EarlyStopping(tf.keras.callbacks.Callback):
    """Early stopping por macro-F1 evaluando tensor pre-cargado."""
    def __init__(self, X_val, y_val, patience=5):
        super().__init__()
        self.X_val_tensor = tf.constant(X_val.astype(np.float32))
        self.y_val        = y_val
        self.patience     = patience
        self.best_f1      = -np.inf
        self.wait         = 0
        self.best_weights = None
        self.stopped_epoch = 0

    def on_epoch_end(self, epoch, logs=None):
        preds  = self.model(self.X_val_tensor, training=False)
        y_pred = np.argmax(tf.cast(preds[1], tf.float32).numpy(), axis=1)
        f1 = f1_score(self.y_val, y_pred, average='macro', zero_division=0)
        if logs is not None:
            logs['val_macro_f1'] = f1
        if f1 > self.best_f1:
            self.best_f1      = f1
            self.wait         = 0
            self.best_weights = self.model.get_weights()
        else:
            self.wait += 1
            if self.wait >= self.patience:
                self.stopped_epoch    = epoch
                self.model.stop_training = True
                if self.best_weights is not None:
                    self.model.set_weights(self.best_weights)

    def get_best_f1(self):
        return self.best_f1

